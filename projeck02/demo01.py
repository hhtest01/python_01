a = 3
b = 17
#算数运算符
#加
print(a+b)
#减
print(a-b)
#乘
print(a*b)
#除（浮点）
print(a/b)
#除（整数）
print(a//b)
#取余
print(a%b)
#次方
print(a**b)
#比较运算符
#大于
print(a>b)
#大于等于
print(a>=b)
#小于
print(a<b)
#小于等于
print(a<=b)
#等于
print(a==b)
#不等于
print(a!=b)
#两个对象引用相等
print(a is b)
#两个对象引用不相等
print(a is not b)
#赋值运算符
#加赋值
a+=b
print(a)
#减赋值
a-=b
print(a)
#乘赋值
a*=b
print(a)
#除赋值
a/=b
print(a)
#取余数赋值
a%=b
print(a)
#取幂赋值
a**=b
print(a)
#取整数赋值
a//=b
print(a)
#逻辑运算符
#a=3,b=17
#逻辑与，都为真时返真
print(a>0 and b>15)
#逻辑或，其中一个为真时返回真
print(a>0 or b>17)
#逻辑非，a为真时，结果为假
print(not a)

